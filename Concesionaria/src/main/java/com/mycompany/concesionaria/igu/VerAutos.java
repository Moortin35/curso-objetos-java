    //Hacemos que la tabla no sea editable
    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        //Ponemos titulos a las tablas
        String titulos[] = {"Id", "Modelo", "Marca", "Motor", "Color", "Patente",
        "Puertas"};
        modeloTabla.setColumnIdentifiers(titulos);
        tablaAutos.setModel(modeloTabla);
        
        //Traemos la lista de autos a la tabla desde la base de datos
        List <Automovil> listaAutos = controladoraLog.traerListaAutos();
        
        //Si mi lista de autos no esta vacía entonces:
        if(listaAutos != null){
            //Por cada auto de mi lista de autos
            for(Automovil auto : listaAutos){
                /* Asigno a la lista objeto los valores de auto para ir
                 asignando posteriomente a la tabla */
               Object[] objeto = {auto.getId(), auto.getModelo(), 
                   auto.getMarca(), auto.getMotor(), auto.getColor(),
                    auto.getPatente(), auto.getCantidadPuertas()};
               //Asigno los valores a una fila de la tabla.
               modeloTabla.addRow(objeto);
            }
        }
        
    }
    
    public void mostrarMensaje(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
        
